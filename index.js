function add(num1, num2){
  return num1 + num2
}
function subtract(num1, num2){
  return num1 - num2
} 
function multiply(num1, num2){
  return num1 * num2
}
function divide(num1, num2){
  return num1 / num2
} 
function inc(n){
  n++
  return n
}
function dec(n){
  n--
  return n--
}
